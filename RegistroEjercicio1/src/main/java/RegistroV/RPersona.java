    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
          boolean personaEncontrada = false;
        String personaSerch = txtNombre.getText();
    
        for(int i =0; i<Main.personas.size(); i++){
            if(Main.personas.get(i).getNombre().equals(personaSerch)){
                txtNombre.setText(Main.personas.get(i).getNombre());
                txtApellido.setText(Main.personas.get(i).getApellido());
                txtCedula.setText(Main.personas.get(i).getCedula());
                txtContacto.setText(Main.personas.get(i).getContacto());
                personaEncontrada =true;
            }
        }
            
        if(personaEncontrada == false){
        Alerta("No se encontrÃ³ esa persona en el registro!", "Error"); 
        
        txtNombre.setText("");
        txtApellido.setText("");
        txtCedula.setText("");
        txtContacto.setText("");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

